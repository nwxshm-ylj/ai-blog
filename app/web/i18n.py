def get_lang(request: Request) -> str:
    lang = request.query_params.get("lang", DEFAULT_LANGUAGE).lower()
    return lang if lang in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def translate(request: Request, key: str) -> str:
    lang = get_lang(request)
    return TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANGUAGE]).get(
        key,
        TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key),
    )


def url_with_lang(
    request: Request,
    path: str,
    params: dict[str, Any] | None = None,
    lang: str | None = None,
) -> str:
    selected_lang = lang if lang in SUPPORTED_LANGUAGES else get_lang(request)
    query: dict[str, Any] = {}
    if selected_lang != DEFAULT_LANGUAGE or "lang" in request.query_params:
        query["lang"] = selected_lang
    if params:
        query.update({key: value for key, value in params.items() if value is not None})
    return f"{path}?{urlencode(query)}" if query else path


def switch_lang_url(request: Request, lang: str) -> str:
    selected_lang = lang if lang in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE
    query = dict(request.query_params)
    if selected_lang == DEFAULT_LANGUAGE:
        query.pop("lang", None)
    else:
        query["lang"] = selected_lang
    return f"{request.url.path}?{urlencode(query)}" if query else request.url.path


